class Contact {
  id;

  get name() {
    return this.name;
  }
  set name(name) {
    this.name = name;
  }

  get address() {
    return this.address;
  }
  set address(address) {
    this.address = address;
  }

  get phone() {
    return this.phone;
  }
  set phone(phone) {
    this.phone = phone;
  }

  get city() {
    return this.city;
  }
  set city(city) {
    this.city = city;
  }

  get state() {
    return this.state;
  }
  set state(state) {
    this.state = state;
  }

  get pincode() {
    return this.pincode;
  }
  set pincode(pincode) {
    this.pincode = pincode;
  }
}
